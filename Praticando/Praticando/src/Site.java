public class Site {
    public String nome; 
    public String url;

    
    public Site() {
    }


    public Site(String nome, String url) {
        this.nome = nome;
        this.url = url;
    }


    public void setNome(String nome) {
        this.nome = nome;
    }


    public void setUrl(String url) {
        this.url = url;
    }

    
}
